void	ex04_ft_putchar(char c)
{
	write(1, &c, 1);
}

int	ft_if_negative(int n)
{
	if (n <= 0)
		ex04_ft_putchar('N');
	else
		ex04_ft_putchar('P');
	return (0);
}
