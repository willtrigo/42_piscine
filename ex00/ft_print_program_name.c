#include <unistd.h>

void	ft_putstr(char *str);

int	main(int agrc, char **agrv)
{
	int	i;

	i = 0;
	if (i < agrc)
		ft_putstr(agrv[i]);
	return (0);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}
